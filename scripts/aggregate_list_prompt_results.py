"""
Aggregate list-prompt rerun results into Ch.6 comparison tables.

Part of PhD Thesis: Triple-Hybrid RAG with PPO-based L-DWA
Author: Shin Dong-wook <sdw@hoseo.ac.kr>

Reads results/rerun_*_list.json (R-DWA, Oracle, L-DWA seeds 42/123/999)
and produces:
  - m8_list_prompt_summary.md — headline comparison table, by-type breakdown,
    3-seed std for L-DWA, and a sentence-vs-list delta row showing what
    the prompt change recovered.

Also reads results/rerun_*_fixed.json (sentence-prompt, post-dff7dc1) for
the sentence side of the delta.

Usage:
    python scripts/aggregate_list_prompt_results.py
"""

from __future__ import annotations

import json
import statistics
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
RESULTS = ROOT / "results"

LIST_FILES = {
    "R-DWA": "rerun_rdwa_list.json",
    "Oracle": "rerun_oracle_list.json",
    "L-DWA (seed 42)": "rerun_ldwa_seed42_list.json",
    "L-DWA (seed 123)": "rerun_ldwa_seed123_list.json",
    "L-DWA (seed 999)": "rerun_ldwa_seed999_list.json",
}

SENTENCE_FILES = {
    "R-DWA": "rerun_rdwa_fixed.json",
    "Oracle": "rerun_oracle_fixed.json",
    "L-DWA (seed 42)": "rerun_ldwa_seed42_fixed.json",
}


def load_agg(path: Path) -> dict:
    with path.open(encoding="utf-8") as f:
        return json.load(f)["aggregate"]


def fmt(mean: float, std: float | None = None, n_digits: int = 4) -> str:
    if std is None:
        return f"{mean:.{n_digits}f}"
    return f"{mean:.{n_digits}f} ± {std:.{n_digits}f}"


def overall_row(name: str, agg: dict) -> dict:
    o = agg["overall"]
    return {
        "policy": name,
        "F1_strict": f'{o["F1_strict"]["mean"]:.4f} ± {o["F1_strict"]["std"]:.4f}',
        "F1_substring": f'{o["F1_substring"]["mean"]:.4f} ± {o["F1_substring"]["std"]:.4f}',
        "F1_char": f'{o.get("F1_char", {"mean": 0, "std": 0})["mean"]:.4f} ± {o.get("F1_char", {"mean": 0, "std": 0})["std"]:.4f}',
        "EM_norm": f'{o["EM_norm"]["mean"]:.4f}',
        "Faithfulness": f'{o["Faithfulness"]["mean"]:.4f} ± {o["Faithfulness"]["std"]:.4f}',
        "Latency_s": f'{o["Latency"]["mean"]:.3f}',
    }


def _load_if_exists(name_to_file: dict[str, str], folder: Path) -> dict[str, dict]:
    out = {}
    for name, fname in name_to_file.items():
        p = folder / fname
        if p.exists():
            try:
                out[name] = load_agg(p)
            except Exception as e:  # noqa: BLE001
                print(f"WARN: failed to load {p}: {e}", file=sys.stderr)
    return out


def three_seed_stats(aggs: list[dict], metric: str) -> tuple[float, float]:
    vals = [a["overall"][metric]["mean"] for a in aggs]
    return (statistics.mean(vals), statistics.stdev(vals) if len(vals) > 1 else 0.0)


def main() -> int:
    list_aggs = _load_if_exists(LIST_FILES, RESULTS)
    sent_aggs = _load_if_exists(SENTENCE_FILES, RESULTS)

    if not list_aggs:
        print(
            "No list-prompt rerun results found. Run evaluate_rerun.py first.",
            file=sys.stderr,
        )
        return 1

    lines: list[str] = [
        "# M8 — List-Prompt Full Rerun Summary (5,000 QA)",
        "",
        "Generated by `scripts/aggregate_list_prompt_results.py`.",
        "",
        "## Headline — list-prompt results",
        "",
        "| Policy | F1_strict | F1_substring | F1_char | EM_norm | Faithfulness | Latency (s) |",
        "|---|---|---|---|---|---|---|",
    ]
    for name in ["R-DWA", "Oracle", "L-DWA (seed 42)", "L-DWA (seed 123)", "L-DWA (seed 999)"]:
        if name in list_aggs:
            r = overall_row(name, list_aggs[name])
            lines.append(
                f'| **{r["policy"]}** | {r["F1_strict"]} | {r["F1_substring"]} | '
                f'{r["F1_char"]} | {r["EM_norm"]} | {r["Faithfulness"]} | {r["Latency_s"]} |'
            )

    # 3-seed stats
    ldwa_aggs = [
        list_aggs[n]
        for n in ("L-DWA (seed 42)", "L-DWA (seed 123)", "L-DWA (seed 999)")
        if n in list_aggs
    ]
    if len(ldwa_aggs) >= 2:
        lines += [
            "",
            "## L-DWA 3-seed consistency (list prompt)",
            "",
            "| Metric | Mean | Std (3 seeds) |",
            "|---|---|---|",
        ]
        for metric_key, display in [
            ("F1_strict", "F1_strict"),
            ("F1_substring", "F1_substring"),
            ("F1_char", "F1_char"),
            ("EM_norm", "EM_norm"),
            ("Faithfulness", "Faithfulness"),
        ]:
            try:
                m, s = three_seed_stats(ldwa_aggs, metric_key)
                lines.append(f"| {display} | {m:.4f} | ± {s:.4f} |")
            except KeyError:
                continue

    # Sentence vs list delta (on R-DWA and Oracle and L-DWA seed 42)
    if sent_aggs:
        lines += [
            "",
            "## Prompt-style delta (sentence → list)",
            "",
            "| Policy | F1_strict (sent → list) | F1_substring | F1_char | EM_norm | Faithfulness |",
            "|---|---|---|---|---|---|",
        ]
        for name in ("R-DWA", "Oracle", "L-DWA (seed 42)"):
            if name in list_aggs and name in sent_aggs:
                s_o = sent_aggs[name]["overall"]
                l_o = list_aggs[name]["overall"]
                def delta(m: str) -> str:
                    sm = s_o.get(m, {"mean": 0.0})["mean"]
                    lm = l_o.get(m, {"mean": 0.0})["mean"]
                    if sm == 0.0 and lm == 0.0:
                        return "0.0 → 0.0 (0%)"
                    if sm == 0.0:
                        return f"0.0 → {lm:.4f} (new)"
                    pct = (lm - sm) / sm * 100
                    return f"{sm:.4f} → {lm:.4f} ({pct:+.1f}%)"
                lines.append(
                    f"| **{name}** | {delta('F1_strict')} | {delta('F1_substring')} | "
                    f"{delta('F1_char')} | {delta('EM_norm')} | {delta('Faithfulness')} |"
                )

    # By-type (R-DWA and L-DWA seed 42)
    if "R-DWA" in list_aggs and "L-DWA (seed 42)" in list_aggs:
        lines += [
            "",
            "## By query type (list prompt)",
            "",
            "| Type | Policy | F1_strict | F1_substring | F1_char | EM_norm | Faithfulness |",
            "|---|---|---|---|---|---|---|",
        ]
        for qtype in ("simple", "multi_hop", "conditional"):
            for name in ("R-DWA", "L-DWA (seed 42)", "Oracle"):
                if name not in list_aggs:
                    continue
                bt = list_aggs[name]["by_type"].get(qtype)
                if bt is None:
                    continue
                lines.append(
                    f'| {qtype} | {name} | {bt["F1_strict"]["mean"]:.4f} | '
                    f'{bt["F1_substring"]["mean"]:.4f} | '
                    f'{bt.get("F1_char", {"mean": 0})["mean"]:.4f} | '
                    f'{bt["EM_norm"]["mean"]:.4f} | '
                    f'{bt["Faithfulness"]["mean"]:.4f} |'
                )

    lines += [
        "",
        "## Interpretation notes",
        "",
        "- **F1_strict jump (sentence → list)** measures how much of the gap "
        "to JKSCI 0.86 is closed by format alignment alone, holding the "
        "evaluator and retrieval fixed.",
        "- **Faithfulness drop (sentence → list)** is expected: list branch "
        "checks each comma-separated claim individually, exposing "
        "hallucinated names that the old 'any-token' sentence check hid. "
        "This is a tighter, not a worse, metric (see Ch.6 box 6-4).",
        "- **L-DWA 3-seed std** quantifies reproducibility. <0.01 is "
        "excellent; thesis claims that L-DWA training is stable.",
        "- **Conditional per-type gain** is the strongest per-type story "
        "(unchanged from M7) — check that the list prompt preserves it.",
    ]

    out = RESULTS / "m8_list_prompt_summary.md"
    out.write_text("\n".join(lines) + "\n", encoding="utf-8")
    print(f"Wrote {out}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
